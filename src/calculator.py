"""
This module provides a function for adding two numbers.
"""

def add_numbers(a, b):
    """
    This function adds two numbers and returns the sum.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The sum of a and b.
    """
    result = a + b  # Changed 'sum' to 'result'
    return result
